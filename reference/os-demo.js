const os = require('os');

// Platform
console.log(os.platform());

// CPU Architeture
console.log(os.arch());

// CPU Core Info
console.log(os.cpus());

// Free memory
console.log(os.freemem());

// Total Memory
console.log(os.totalmem());

// Home Directory
console.log(os.homedir());

// Uptime (in seconds)
console.log(os.uptime())

